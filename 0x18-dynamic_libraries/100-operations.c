#include <stdio.h>

/**
 * add - sums two numbers
 * @a: first number
 * @b: second number
 *
 * Return: returns sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first number
 * @b: second number
 *
 * Return: returns subraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: second number
 *
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of the division of
 * two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: returns the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
